public class ZipFileGeneratorException extends Exception {

    public ZipFileGeneratorException() {
        super();
    }

    public ZipFileGeneratorException(String message) {
        super(message);
    }

    public ZipFileGeneratorException(String message, Throwable cause) {
        super(message, cause);
    }

    public ZipFileGeneratorException(Throwable cause) {
        super(cause);
    }
}
